    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
    try {
    java.sql.Connection conn = (java.sql.Connection) koneksi.config.configDB();
    String sql = "UPDATE produk SET nama_produk=?, jumlah_stok=?, harga_produk=?, nama_kategori=? WHERE id_produk=?";
    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    pst.setString(1, txtnamaproduk.getText());
    pst.setInt(2, Integer.parseInt(txtjumlahstok.getText()));
    pst.setInt(3, Integer.parseInt(txthargasatuan.getText()));
    pst.setString(4, (String) cmbkategori.getSelectedItem());
    pst.setInt(5, Integer.parseInt(txtid.getText()));
    pst.executeUpdate();
    JOptionPane.showMessageDialog(null, "Data berhasil di edit");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Data Gagal di edit");
        e.printStackTrace();
    }
    datatable();

    }//GEN-LAST:event_editActionPerformed

    private void cmbcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbcariActionPerformed
    try {
        java.sql.Connection conn=(java.sql.Connection)koneksi.config.configDB();
        String sql = "Select * from produk where nama_produk='"+txtcari.getText()+"'";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery(sql);
        if (rs.next()){
           txtid.setText(rs.getString(1));
           txtnamaproduk.setText(rs.getString(2));
           txtjumlahstok.setText(rs.getString(4));
           txthargasatuan.setText(rs.getString(5));
           cmbkategori.setSelectedItem(rs.getString(3));
        }
        
           JOptionPane.showMessageDialog(null,"Data Produk Dengan Id '"+txtcari.getText()+"' Ditemukan");
    } catch (Exception t){
    JOptionPane.showMessageDialog(null,"Data Produk Dengan Id '"+txtcari.getText()+"' Tidak Ditemukan");
    }
    datatable();
    }//GEN-LAST:event_cmbcariActionPerformed

