public void datatable(){
    DefaultTableModel tbl=new DefaultTableModel();
    tbl.addColumn("Id");
    tbl.addColumn("Nama Produk");
    tbl.addColumn("Kategori");
    tbl.addColumn("Jumlah Stok");
    tbl.addColumn("Harga Satuan"); 
    table.setModel(tbl);
    try{
        Statement statement=(Statement) config.configDB().createStatement();
        ResultSet res=statement.executeQuery("SELECT p.id_produk, p.nama_produk, k.nama_kategori, p.jumlah_stok, p.harga_produk "
                + "FROM produk p LEFT JOIN kategori_produk k ON p.nama_kategori = k.nama_kategori");
        while(res.next())
        {
            tbl.addRow(new Object[]{
                res.getString("id_produk"),
                res.getString("nama_produk"),
                res.getString("nama_kategori"),
                res.getString("jumlah_stok"),
                res.getString("harga_produk")
            });
            table.setModel(tbl);
        }
    }catch (Exception e){
            JOptionPane.showMessageDialog(null,"salah!"+e.getMessage());
    }
    }
