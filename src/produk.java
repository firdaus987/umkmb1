    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
    String id_produk = txtid.getText();
    String nama_produk = txtnamaproduk.getText();
    String harga_produk = txthargasatuan.getText();
    String nama_kategori = (String) cmbkategori.getSelectedItem();
    String jumlah_stok = txtjumlahstok.getText();
    try{
        Statement statement = (Statement) config.configDB().createStatement();
        statement.executeUpdate("insert into produk(id_produk, nama_produk, harga_produk, nama_kategori, jumlah_stok) VALUES ('"+id_produk+"','"+nama_produk+"','"+harga_produk+"','"+nama_kategori+"','"+jumlah_stok+"')");
        statement.close();
        JOptionPane.showMessageDialog(null, "data berhasil ditambah!");
    }catch (Exception t){
         t.printStackTrace();
        JOptionPane.showMessageDialog(null, "data gagal ditambah!");
    }
    datatable();
 
    }//GEN-LAST:event_tambahActionPerformed

