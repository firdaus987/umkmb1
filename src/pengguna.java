    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
    try {
    java.sql.Connection conn = (java.sql.Connection) koneksi.config.configDB();
    String sql = "UPDATE pengguna SET nama=?, alamat=?, id_role=? WHERE no_telp=?";
    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    pst.setString(1, txtnama.getText());
    pst.setString(2, txtalamat.getText());
    pst.setString(3, (String) cmbrole.getSelectedItem());
    pst.setString(4, txtnotelp.getText());
    pst.executeUpdate();
    JOptionPane.showMessageDialog(null, "Data berhasil di edit");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Data Gagal di edit");
        e.printStackTrace();
    }
    datatable();
    }//GEN-LAST:event_editActionPerformed

