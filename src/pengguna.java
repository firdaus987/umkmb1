public void datatable(){
    DefaultTableModel tbl=new DefaultTableModel();
    //tbl.addColumn("Id");
    tbl.addColumn("Nama");
    tbl.addColumn("Alamat");
    tbl.addColumn("No Telpon");
    tbl.addColumn("Role");
    table.setModel(tbl);
    try{
        Statement statement=(Statement) config.configDB().createStatement();
        ResultSet res=statement.executeQuery("SELECT u.nama, u.alamat, u.no_telp, u.id_role FROM pengguna u LEFT JOIN role r ON u.id_role = r.id_role");
        while(res.next())
        {
            tbl.addRow(new Object[]{
                //res.getString("id"),
                res.getString("nama"),
                res.getString("alamat"),
                res.getString("no_telp"),
                res.getString("id_role")
            });
            table.setModel(tbl);
        }
    }catch (Exception e){
            JOptionPane.showMessageDialog(null,"salah!"+e.getMessage());
    }
    }
