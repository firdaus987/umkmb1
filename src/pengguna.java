    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
    String cari = txtcari.getText();
    try{
        Statement statement = (Statement) config.configDB().createStatement();
        statement.executeUpdate("DELETE from pengguna where nama=('"+cari+"');");
        JOptionPane.showMessageDialog(null,"data berhasil di HAPUS!");
        txtnama.setText("");
        txtnama.setText("");
        txtalamat.setText("");
        txtnotelp.setText("");
        txtnama.requestFocus();
    }catch (Exception t){
        JOptionPane.showMessageDialog(null,"Data gagal di HAPUS!");
    }
    datatable();
    }//GEN-LAST:event_hapusActionPerformed

    private void cmbcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbcariActionPerformed
        try {
            java.sql.Connection conn=(java.sql.Connection)koneksi.config.configDB();
            String sql = "Select * from pengguna where nama='"+txtcari.getText()+"'";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            if (rs.next())
            //txtid.setText(rs.getString(1));
            txtnama.setText(rs.getString(3));
            txtalamat.setText(rs.getString(4));
            txtnotelp.setText(rs.getString(5));
            cmbrole.setSelectedItem(rs.getString(6));
            JOptionPane.showMessageDialog(null,"Data Pengguna Dengan nama '"+txtcari.getText()+"' Ditemukan");
        } catch (Exception t){
            JOptionPane.showMessageDialog(null,"Data Pengguna Dengan nama '"+txtcari.getText()+"' Tidak Ditemukan");
        }
        datatable();
    }//GEN-LAST:event_cmbcariActionPerformed

