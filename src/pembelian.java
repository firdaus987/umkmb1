    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
    String jenis_pembelian = (String) cmbjenis.getSelectedItem();
    String tagihan = txttagihan.getText();
    String keterangan = txtketerangan.getText();
    try{
        Statement statement = (Statement) config.configDB().createStatement();
        statement.executeUpdate("insert into pembelian(jenis_pembelian, tagihan, keterangan) VALUES ('"+jenis_pembelian+"','"+tagihan+"','"+keterangan+"')");
        statement.close();
        JOptionPane.showMessageDialog(null, "data berhasil ditambah!");
    }catch (Exception t){
    t.printStackTrace();
    JOptionPane.showMessageDialog(null, "data gagal ditambah!");
    }
    datatable();
 
    }//GEN-LAST:event_tambahActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
    String id_pembelian = txttagihan.getText();
    try{
        Statement statement = (Statement) config.configDB().createStatement();
        statement.executeUpdate("DELETE from pembelian where id_pembelian=('"+id_pembelian+"');");
        JOptionPane.showMessageDialog(null,"data berhasil di HAPUS!");
        txttagihan.setText("");
        txtketerangan.setText("");
        txttagihan.requestFocus();
    }catch (Exception t){
        JOptionPane.showMessageDialog(null,"Data gagal di HAPUS!");
    }
    datatable();
    }//GEN-LAST:event_hapusActionPerformed

    private void cmbcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbcariActionPerformed
        try {
            java.sql.Connection conn=(java.sql.Connection)koneksi.config.configDB();
            String sql = "Select * from pembelian where id_pembelian='"+txtcari.getText()+"'";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            if (rs.next())
            cmbjenis.setSelectedItem(rs.getString(5));
            txttagihan.setText(rs.getString(4));
            txtketerangan.setText(rs.getString(3));
            
            JOptionPane.showMessageDialog(null,"Data Pembelian Dengan Id '"+txtcari.getText()+"' Ditemukan");
        } catch (Exception t){
            JOptionPane.showMessageDialog(null,"Data Pembelian Dengan Id '"+txtcari.getText()+"' Tidak Ditemukan");
        }
        datatable();
    }//GEN-LAST:event_cmbcariActionPerformed

