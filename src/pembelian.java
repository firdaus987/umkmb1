public void datatable(){
    DefaultTableModel tbl=new DefaultTableModel();
    tbl.addColumn("Id Pembelian");
    tbl.addColumn("Tgl Pembelian");
    tbl.addColumn("Jenis Pembelian");
    tbl.addColumn("Tagihan");
    tbl.addColumn("Keterangan");
    table.setModel(tbl);
    try{
        Statement statement=(Statement) config.configDB().createStatement();
        ResultSet res=statement.executeQuery("SELECT b.id_pembelian, b.tgl_pembelian, b.jenis_pembelian, b.tagihan, b.keterangan "
                + "FROM pembelian b LEFT JOIN jenis_beli g ON b.id_pembelian = g.id_beli");
        while(res.next())
        {
            tbl.addRow(new Object[]{
                res.getString("id_pembelian"),
                res.getString("tgl_pembelian"),
                res.getString("jenis_pembelian"),
                res.getString("tagihan"),
                res.getString("keterangan")
            });
            table.setModel(tbl);
        }
    }catch (Exception e){
            JOptionPane.showMessageDialog(null,"salah!"+e.getMessage());
    }
    }

