    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        try {
            java.sql.Connection conn = (java.sql.Connection) koneksi.config.configDB();
//            String sql = "UPDATE pembelian SET jenis_pembelian=?, tagihan=?, keterangan=? WHERE id_pembelian=?";
            String sql = "UPDATE pembelian SET jenis_pembelian=?, tagihan=?, keterangan=?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, (String) cmbjenis.getSelectedItem());
            pst.setString(2, txttagihan.getText());
            pst.setString(3, txtketerangan.getText());           
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil di edit");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal di edit");
            e.printStackTrace();
        }
        datatable();
    }//GEN-LAST:event_editActionPerformed

